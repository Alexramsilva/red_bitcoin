df = pd.DataFrame(data, columns=["day", "time"])


# -------------------------
# 2. Feature engineering
# -------------------------
def time_to_minutes(t):
    h, m = map(int, t.split(":"))
    return h * 60 + m

df["minutes"] = df["time"].apply(time_to_minutes)

# Variables cíclicas (muy importante)
df["hour_sin"] = np.sin(2 * np.pi * df["minutes"] / 1440)
df["hour_cos"] = np.cos(2 * np.pi * df["minutes"] / 1440)

df["day_sin"] = np.sin(2 * np.pi * df["day"] / 7)
df["day_cos"] = np.cos(2 * np.pi * df["day"] / 7)

df["target"] = 1  # oportunidades observadas

# -------------------------
# 3. Generar datos negativos
# -------------------------
np.random.seed(42)
neg_samples = 200

neg_day = np.random.randint(1, 8, neg_samples)
neg_min = np.random.randint(0, 1440, neg_samples)

neg_df = pd.DataFrame({
    "day": neg_day,
    "minutes": neg_min,
    "target": 0
})

neg_df["hour_sin"] = np.sin(2 * np.pi * neg_df["minutes"] / 1440)
neg_df["hour_cos"] = np.cos(2 * np.pi * neg_df["minutes"] / 1440)
neg_df["day_sin"] = np.sin(2 * np.pi * neg_df["day"] / 7)
neg_df["day_cos"] = np.cos(2 * np.pi * neg_df["day"] / 7)

# Unir dataset
df_all = pd.concat([df, neg_df])

X = df_all[["hour_sin", "hour_cos", "day_sin", "day_cos"]]
y = df_all["target"]

# -------------------------
# 4. Escalamiento
# -------------------------
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# -------------------------
# 5. Modelo NN
# -------------------------
model = MLPClassifier(hidden_layer_sizes=(10, 10),
                      activation='relu',
                      max_iter=1000)

model.fit(X_scaled, y)

# -------------------------
# 6. UI Streamlit
# -------------------------
st.title("📈 Bitcoin Trading Opportunity Predictor")


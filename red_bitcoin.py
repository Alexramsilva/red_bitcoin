minutes = hour * 60 + minute

# Transformación
input_data = np.array([[
    np.sin(2 * np.pi * minutes / 1440),
    np.cos(2 * np.pi * minutes / 1440),
    np.sin(2 * np.pi * day / 7),
    np.cos(2 * np.pi * day / 7)
]])

input_scaled = scaler.transform(input_data)

